    private void removeEmployeeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeEmployeeButtonActionPerformed
        int selectedRow = employeeTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete this employee?", "Warning", JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                Employee e = (Employee) employeeTable.getValueAt(selectedRow, 1);
                foundation.getStaffDirectory().getEmployeeDirectory().removeEmployee(e);
                Type type = (Type) deptCombox.getSelectedItem();
                popEmoloyeeTable(type);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a row from table", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_removeEmployeeButtonActionPerformed

    private void createEmployeeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEmployeeButtonActionPerformed
        String firstName = firstNameText.getText().trim();
        String lastName = lastNameText.getText().trim();
        String userName = jLabelUserName.getText().trim();
        String password = String.valueOf(passwordText.getPassword());
        String cpassword = String.valueOf(confirmPasswordText.getPassword());
        
        if(!password.equals(cpassword))
        {
            JOptionPane.showMessageDialog(this, "Passwords doesn't match!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(firstName.equals("") || lastName.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter first name and last name", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            Date birthday = dateChooserCombo1.getSelectedDate().getTime();
            Type type = (Type) deptCombox.getSelectedItem();
            if(type == Employee.Type.Nutrition)
            {
                NutritionEmployee ne = new NutritionEmployee(firstName, lastName, birthday);            
                UserAccount ua = foundation.getUserAccountDirectory().addUserAccount(userName, password, ne, new NutritionRole());
                foundation.getStaffDirectory().getEmployeeDirectory().addEmployee(ne);
            }
            else if(type == Employee.Type.FoundationFinancial)
            {
                FinancialEmployee fe = new FinancialEmployee(firstName, lastName, birthday);
                UserAccount ua = foundation.getUserAccountDirectory().addUserAccount(userName, password, fe, new FoundationFinancialRole());
                foundation.getStaffDirectory().getEmployeeDirectory().addEmployee(fe);
            }
            popEmoloyeeTable(type);
        }
    }//GEN-LAST:event_createEmployeeButtonActionPerformed

