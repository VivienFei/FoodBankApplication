    private void popEmoloyeeTable(Type type)
    {
        DefaultTableModel dtm = (DefaultTableModel)employeeTable.getModel();
        dtm.setRowCount(0);
                   
        for (Employee employee : foundation.getStaffDirectory().getEmployeeDirectory().getEmployees())
        {
            if(employee.getDepartment().equals(type.getValue()))
            {
                Object row[] = new Object[2];
                row[0] = employee.getId();
                row[1] = employee;
                dtm.addRow(row);
            }
        } 
    }
