    private void updateEmployeeInfoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateEmployeeInfoButtonActionPerformed
        int selectedRow = employeeTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            Employee e = (Employee) employeeTable.getValueAt(selectedRow, 1);
            EmployeeInfoJPanel mijp = new EmployeeInfoJPanel(userProcessContainer, e);
            userProcessContainer.add("mijp", mijp);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a row from table", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateEmployeeInfoButtonActionPerformed


