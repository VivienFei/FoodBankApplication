    private void popNetworkCombox()
    {
        networkComboBox.removeAllItems();
        
        for (Network network : foundation.getNetworks().getNetworks())
        {
            networkComboBox.addItem(network);
        }        
    }

    
    private void popFoodBankTable()
    {
        Network network = (Network)networkComboBox.getSelectedItem();
        
        DefaultTableModel dtm = (DefaultTableModel)foodBankTable.getModel();
        dtm.setRowCount(0);
                   
        for (FoodBank foodbank : network.getFoodBankDirectory().getFoodBanks())
        {
            Object row[] = new Object[2];
            row[0] = foodbank;
            row[1] = network.getName();
            dtm.addRow(row);
        }            
    }
