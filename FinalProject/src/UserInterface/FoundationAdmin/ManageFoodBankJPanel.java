    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        Network network = (Network)networkComboBox.getSelectedItem();
        FoodBank foodBank = network.getFoodBankDirectory().addFoodBank(nameText.getText());
                                        
        String password = String.valueOf(passwordText.getPassword());
        String pw = String.valueOf(confirmPasswordText.getPassword());
        if(password.equals(pw))
        {
            UserAccount ua = foodBank.getUserAccountDirectory().addUserAccount(userNameLabel.getText(), password, foodBank, new FoodBankAdminRole());        
            JOptionPane.showMessageDialog(null, "Food Bank and User Account Created");
            popFoodBankTable();
        }else
        {
            JOptionPane.showMessageDialog(null, "Password doesn't match");
        }     
    }//GEN-LAST:event_submitButtonActionPerformed

