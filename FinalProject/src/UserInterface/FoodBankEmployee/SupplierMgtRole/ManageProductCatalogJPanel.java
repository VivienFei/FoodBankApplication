    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String search = ProductNameTXT.getText();
        Supplier supplier=(Supplier)SupplierComboBox.getSelectedItem();
        if (search==null|| search.length() ==0){
            JOptionPane.showMessageDialog(null, "Please type the search name");
            }else{
            Product result = supplier.getProductCatalog().searchProduct(ProductNameTXT.getText());
            if (result==null){
                JOptionPane.showMessageDialog(null,"Your selected product name does not exist!", "Information", JOptionPane.INFORMATION_MESSAGE);
               }
            else{
                //Account account=(Account)tblAccounts.getValueAt(selectedrow,0);
                SearchProductresultJPanel panel = new SearchProductresultJPanel(userProcessContainer,result);
                userProcessContainer.add("ViewAccountJPanel", panel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int row = productCatalogTable.getSelectedRow();

        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Product s = (Product)productCatalogTable.getValueAt(row, 0);
        Supplier supplier=(Supplier)SupplierComboBox.getSelectedItem();
        supplier.getProductCatalog().removeProduct(s);
        populateTable(supplier);
    }//GEN-LAST:event_btnDeleteActionPerformed

