    public void populateTable(Supplier supplier){
        int rowCount = productCatalogTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)productCatalogTable.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        
        for(Product p : supplier.getProductCatalog().getProducts()) {
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getType();
            row[2] = p.getSubstitute();
            model.addRow(row);
        }
        
    }
    
    

