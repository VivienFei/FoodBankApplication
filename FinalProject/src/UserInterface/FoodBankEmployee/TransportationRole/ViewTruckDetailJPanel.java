    private void populateTruckDetails(){
        
        TruckConditionTXT.setText(truck.getCondition());
        purchasePriceTXT.setText(String.valueOf(truck.getPurchasePrice()));
        purchaseConditionTXT.setText(truck.getPurchaseCondition());
        plateNumberTXT.setText(truck.getPlateNum());
        ResposibleDriverName.setText(String.valueOf(truck.getResponsibleDriver().getName()));
        locationTXT.setText(truck.getLocation());
        modelTXT.setText(truck.getModel());
        LoadTXT.setText(String.valueOf(truck.getTruckLoad()));
        NotesTXT.setText(truck.getNote());
        
        
        
        
        
    }
    
    public void populateTruckDriverListCombobox(){
        TruckDriverListCombobox.removeAllItems();
        
        for (Employee driver : Foodbank.getStaffDirectory().getEmployeeDirectory().getEmployees()){
            TruckDriverListCombobox.addItem(String.valueOf(driver));
        }
    }

