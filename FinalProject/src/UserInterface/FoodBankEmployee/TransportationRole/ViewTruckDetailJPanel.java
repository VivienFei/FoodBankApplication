    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        truck.setCondition(TruckConditionTXT.getText());
        truck.setPurchaseCondition(purchaseConditionTXT.getText());
        
        truck.setLocation(locationTXT.getText());
        truck.setModel(modelTXT.getText());
        truck.setNote(NotesTXT.getText());
        truck.setPlateNum(plateNumberTXT.getText());
        truck.setPurchasePrice(Integer.valueOf(purchasePriceTXT.getText()));
        truck.setTruckLoad(Integer.valueOf(LoadTXT.getText()));
        truck.setResponsibleDriver((Person)TruckDriverListCombobox.getSelectedItem());
        JOptionPane.showMessageDialog(null, "Truck successivefully saved");
        btnSave.setEnabled(false);
        updateButton.setEnabled(true);

    }//GEN-LAST:event_btnSaveActionPerformed

