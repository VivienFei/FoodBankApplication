    private void viewEventDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewEventDetailButtonActionPerformed
        int SelectedRow = eventTable.getSelectedRow();
        
        if (SelectedRow >= 0){
            Activity activity = (Activity)eventTable.getValueAt(SelectedRow, 0);
            ViewEventDetailJPanel vedj = new ViewEventDetailJPanel(userProcessContainer, activity, foodbank);
            userProcessContainer.add("ViewEventDetailJPanel", vedj);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
                
    }//GEN-LAST:event_viewEventDetailButtonActionPerformed

