    public void popEventTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)eventTable.getModel();
        dtm.setRowCount(0);
        
        for (Activity activity : foodbank.getActivityDirectory().getActivities())
        {
            if (activity.getType().equals(eventTypeCombox.getSelectedItem()))
            {
                Object[] row = new Object[3];
                row[0] = activity;
                row[1] = activity.getDate();
                row[2] = activity.getName();
                dtm.addRow(row);
            } 
        }       
    }
