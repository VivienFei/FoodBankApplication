    private void popAllInformation()
    {
        Calendar cal = Calendar.getInstance();
        cal.setTime(volunteer.getBirthday());
        
        firstNameText.setText(volunteer.getName().getFirstName());
        lastNameText.setText(volunteer.getName().getLastName());
        birthDateCombo.setSelectedDate(cal);
        txtAddress1.setText(volunteer.getAddress().getFirstLine());
        txtAddress2.setText(volunteer.getAddress().getSecondLine());
        txtCity.setText(volunteer.getAddress().getCity());
        stateComx.setSelectedItem(volunteer.getAddress().getState());
        txtZip.setText(volunteer.getAddress().getZipcode());
        phoneNumberText.setText(volunteer.getPhoneNum());
        emailAddressText.setText(volunteer.getEmail());
        futureContactCheckBox.setSelected(volunteer.isFuturContact());
    }
