    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed

        
        String firstName = firstNameText.getText();
        String lastName = lastNameText.getText();
        Name name = new Name(firstName, lastName);        
        Date birthDate = birthDateCombo.getSelectedDate().getTime();
        
        Address address = new Address();
        address.setFirstLine(txtAddress1.getText());
        address.setFirstLine(txtAddress2.getText());
        address.setCity(txtCity.getText());
        address.setState((String)stateComx.getSelectedItem());
        address.setZipcode(txtZip.getText());
        
        String phoneNumber = phoneNumberText.getText();
        String emailAddress = emailAddressText.getText();
        boolean futureContact = futureContactCheckBox.isSelected();
        
        volunteer.setName(name);
        volunteer.setBirthday(birthDate);
        volunteer.setAddress(address);
        volunteer.setPhoneNum(phoneNumber);
        volunteer.setEmail(emailAddress);
        volunteer.setFuturContact(futureContact);
                
        firstNameText.setEnabled(false);
        birthDateCombo.setEnabled(false);
        txtAddress1.setEnabled(false);
        txtAddress2.setEnabled(false);
        txtCity.setEnabled(false);
        stateComx.setEnabled(false);
        txtZip.setEnabled(false);
        phoneNumberText.setEnabled(false);
        emailAddressText.setEnabled(false);
        futureContactCheckBox.setEnabled(false);
        saveButton.setEnabled(false);
        updateButton.setEnabled(true);
        backButton.setEnabled(true);
        
        JOptionPane.showMessageDialog(null, "Changes saved");

    }//GEN-LAST:event_saveButtonActionPerformed


