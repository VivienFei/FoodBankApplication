        public void popVolunteerTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)volunteerTable.getModel();
        dtm.setRowCount(0);
        
        for(Volunteer volunteer : foodbank.getStaffDirectory().getVolunteerDirectory().getVolunteers())
        {
            Object row[] = new Object[2];
            row[0] = volunteer;
            row[1] = volunteer.getId();
            
            dtm.addRow(row);
        }
    }
