    private void viewDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailButtonActionPerformed
        int SelectedRow = volunteerTable.getSelectedRow();
        
        if (SelectedRow >= 0){
            Volunteer volunteer = (Volunteer)volunteerTable.getValueAt(SelectedRow, 0);
            ViewVolunteerDetailJPanel vvdjp = new ViewVolunteerDetailJPanel(userProcessContainer, volunteer);
            userProcessContainer.add("ViewVolunteerDetailJPanel", vvdjp);

            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
                
    }//GEN-LAST:event_viewDetailButtonActionPerformed

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backButtonActionPerformed

    private void addVolunteerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addVolunteerButtonActionPerformed
        String firstName = firstNameText.getText();
        String lastName = lastNameText.getText();
        Date birthDate = birthDateCombo.getSelectedDate().getTime();
        
        Address address = new Address();
        address.setFirstLine(txtAddress1.getText());
        address.setFirstLine(txtAddress2.getText());
        address.setCity(txtCity.getText());
        address.setState((String)stateComx.getSelectedItem());
        address.setZipcode(txtZip.getText());
        
        String phoneNumber = phoneNumberText.getText();
        String emailAddress = emailAddressText.getText();
        
        Volunteer volunteer = new Volunteer(firstName, lastName, birthDate);
        volunteer.setEmail(emailAddress);
        volunteer.setAddress(address);
        volunteer.setPhoneNum(phoneNumber);
        foodbank.getStaffDirectory().getVolunteerDirectory().addVolunteer(volunteer);
        
        JOptionPane.showMessageDialog(null, "Volunteer added");
        
        popVolunteerTable();
    }//GEN-LAST:event_addVolunteerButtonActionPerformed


