    private void jTreeValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_jTreeValueChanged
        DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) jTree.getLastSelectedPathComponent();
        Organization o = (Organization)selectedNode.getUserObject();
        if(selectedNode != null)
        {
            jLabelSelectedOrganization.setText(selectedNode.toString());
            FoodBank fb = (FoodBank) o;
            jLabelOrganizationFunds.setText(String.valueOf(fb.getFunds()));
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a node from tree", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTreeValueChanged

    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) jTree.getLastSelectedPathComponent();
        if(selectedNode != null)
        {
            FoodBank f = (FoodBank) selectedNode.getUserObject();
            ViewOrganizationInfoJPanel viewOrganizationInfoJPanel = new ViewOrganizationInfoJPanel(userProcessContainer, f);
            userProcessContainer.add("viewOrganizationInfoJPanel", viewOrganizationInfoJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a node from tree", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDetailActionPerformed

    private void btnDistributeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDistributeActionPerformed
        DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) jTree.getLastSelectedPathComponent();
        if(selectedNode != null)
        {
            FoodBank f = (FoodBank) selectedNode.getUserObject();
            try {
                double fund = Double.parseDouble(txtAmount.getText());
                double newFoodBankFund = f.getFunds() + fund;
                f.setFunds(newFoodBankFund);
                double newFund = foundation.getFunds() - fund;
                foundation.setFunds(newFund);
                jLabelAvailableFunds.setText(String.valueOf(foundation.getFunds()));
                jLabelOrganizationFunds.setText(String.valueOf(f.getFunds()));
                
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "Please enter digital!", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a node from tree", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDistributeActionPerformed


