    public void populateTree(){
        DefaultTreeModel model = (DefaultTreeModel) jTree.getModel();
        
        ArrayList<Network> networkList = foundation.getNetworks().getNetworks();
        ArrayList<FoodBank> foodBankList;
        
        Network network;
        FoodBank foodBank;
        
        DefaultMutableTreeNode networks = new DefaultMutableTreeNode("Foundation");
        DefaultMutableTreeNode root = (DefaultMutableTreeNode) model.getRoot();
        root.removeAllChildren();
        root.insert(networks, 0);
        
        DefaultMutableTreeNode networkNode;
        DefaultMutableTreeNode foodBankNode;
        
        for(int i = 0 ; i < networkList.size() ; i++)
        {
             network = networkList.get(i);
             networkNode = new DefaultMutableTreeNode(network.getName());
             networkNode.insert(networkNode, i);
             
             foodBankList = network.getFoodBankDirectory().getFoodBanks();
             
             for(int j = 0 ; j < foodBankList.size() ; j++)
             {
                 foodBank = foodBankList.get(j);
                 foodBankNode = new DefaultMutableTreeNode(foodBank.getName());
                 networkNode.insert(foodBankNode, j);
             }
        }
        model.reload();
    }
    
