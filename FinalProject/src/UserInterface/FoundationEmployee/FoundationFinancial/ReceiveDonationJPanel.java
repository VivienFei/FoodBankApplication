    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTableUnreceived.getModel();
        
        model.setRowCount(0);
        
        for (Donation d : foundation.getUnreceived().getDonations()){
            Object[] row = new Object[4];
            row[0] = d;
            row[1] = d.getQuantity();
            row[2] = d.getNetwork();
            row[3] = d.getFoodBank();
            model.addRow(row);
        }
    }
    
