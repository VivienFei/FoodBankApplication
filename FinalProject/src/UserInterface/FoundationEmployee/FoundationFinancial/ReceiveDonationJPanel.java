    private void btnReceiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReceiveActionPerformed
        int selectedRow = jTableUnreceived.getSelectedRow();
        if(selectedRow >= 0)
        {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to add this donation?", "Warning", JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                Donation d = (Donation) jTableUnreceived.getValueAt(selectedRow, 0);
                d.setReceiveDate(Calendar.getInstance().getTime());
                foundation.getDonationList().addDonation(d);
                foundation.getUnreceived().removeDonation(d);
        
                populateTable();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a row from table", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnReceiveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = jTableUnreceived.getSelectedRow();
        if(selectedRow >= 0)
        {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete this donation?", "Warning", JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                Donation d = (Donation) jTableUnreceived.getValueAt(selectedRow, 0);
                foundation.getUnreceived().removeDonation(d);
        
                populateTable();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a row from table", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        int selectedRow = jTableUnreceived.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            Donation d = (Donation) jTableUnreceived.getValueAt(selectedRow, 0);
            
            DonationDetailJPanel donationDetailJPanel = new DonationDetailJPanel(userProcessContainer, d);
            userProcessContainer.add("donationDetailJPanel", donationDetailJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a row from table", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDetailActionPerformed

