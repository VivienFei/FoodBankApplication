    private void removeEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeEmployeeActionPerformed
        int selectedRow = EmployeeTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete this employee?", "Warning", JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                Employee e = (Employee) EmployeeTable.getValueAt(selectedRow, 1);
                Foodbank.getStaffDirectory().getEmployeeDirectory().removeEmployee(e);
                Employee.Type type = (Employee.Type) deptCombox.getSelectedItem();
                popEmoloyeeTable(type);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select an employee", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_removeEmployeeActionPerformed

    private void createEmployeeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEmployeeButtonActionPerformed

        String firstName = firstNameText.getText().trim();
        String lastName = lastNameText.getText().trim();
        String userName = userNameLabel.getText().trim();
        String password = String.valueOf(pwPassword.getPassword());
        String cpassword = String.valueOf(confirmPwPassword.getPassword());
                
        
        if(!password.equals(cpassword))
        {
            JOptionPane.showMessageDialog(this, "Password doesn't match!", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(firstName.equals("") || lastName.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter first name and last name", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else
        {   
            Date birthday = dateChooserCombox.getSelectedDate().getTime();
            Type type = (Type) deptCombox.getSelectedItem();
            if(type != null)
            switch (type) {
                case ActivityManagement:{
                    ActivityManagementEmployee ne = new ActivityManagementEmployee(firstName, lastName, birthday);
                    UserAccount ua = Foodbank.getUserAccountDirectory().addUserAccount(userName, password, ne, new ActivityManagementRole());
                    Foodbank.getStaffDirectory().getEmployeeDirectory().addEmployee(ne);
                        break;
                    }
                case ShelterManagement:{
                    ShelterManagementEmployee fe = new ShelterManagementEmployee(firstName, lastName, birthday);
                    UserAccount ua = Foodbank.getUserAccountDirectory().addUserAccount(userName, password, fe, new ShelterManagementRole());
                    Foodbank.getStaffDirectory().getEmployeeDirectory().addEmployee(fe);
                        break;
                    }
                case SupplierManagement:{
                    SupplierManagementEmployee fe = new SupplierManagementEmployee(firstName, lastName, birthday);
                    UserAccount ua = Foodbank.getUserAccountDirectory().addUserAccount(userName, password, fe, new SupplierManagementRole());
                    Foodbank.getStaffDirectory().getEmployeeDirectory().addEmployee(fe);
                        break;
                    }
                case Transportation:{
                    TransportationEmployee fe = new TransportationEmployee(firstName, lastName, birthday);
                    UserAccount ua = Foodbank.getUserAccountDirectory().addUserAccount(userName, password, fe, new TransportationRole());
                    Foodbank.getStaffDirectory().getEmployeeDirectory().addEmployee(fe);
                        break;
                    }
                default:
                    break;
            } 
            popEmoloyeeTable(type);
            JOptionPane.showMessageDialog(null, "Employee Successfully added", "Warning'", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_createEmployeeButtonActionPerformed

    private void updateEmployeeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateEmployeeButtonActionPerformed
        int selectedRow = EmployeeTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            Employee e = (Employee) EmployeeTable.getValueAt(selectedRow, 1);
            EmployeeInfoJPanel mijp = new EmployeeInfoJPanel(userProcessContainer, e);
            userProcessContainer.add("mijp", mijp);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select an employee from table", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateEmployeeButtonActionPerformed

