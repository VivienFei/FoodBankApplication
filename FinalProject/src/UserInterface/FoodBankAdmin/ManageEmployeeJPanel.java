    private void popEmoloyeeTable(Employee.Type type)
    {
        DefaultTableModel dtm = (DefaultTableModel)EmployeeTable.getModel();
        dtm.setRowCount(0);
                   
        for (Employee employee : Foodbank.getStaffDirectory().getEmployeeDirectory().getEmployees())
        {
            if(employee.getDepartment().endsWith(type.getValue()))
            {
                Object row[] = new Object[2];
                row[0] = employee.getId();
                row[1] = employee;
                dtm.addRow(row);
            }
        } 
    }
    

